def pengurangan(a, b) :
    c = a - b
    print(c)

def penjumlahan(a, b) :
    c = a + b
    return c

##====Sampai sini saja===##

def main():
    # tanpa return
    pengurangan(50, 22)

    # dengan return
    jumlah = penjumlahan(6, 7)
    print(jumlah)


if __name__ == "__main__":
    main()



